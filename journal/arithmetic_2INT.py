# WAP to enter two integers and perform all arithmetic operations
a = int(input("Enter the 1st number:  "))
b = int(input("Enter the 2nd number:  "))

addition = a+b
difference = a-b
division = a/b
multi = a*b
mod = a%b

print("The addition of two numbers is:  ", addition)
print("The difference of two numbers is:  ", difference)
print("The division of two numbers is:  ", division)
print("The multi of two numbers is:  ", multi)
print("The mod of two numbers is:  ", mod)
